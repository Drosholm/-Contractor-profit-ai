import PDFDocument from "pdfkit";

export default async function handler(req, res) {

  if (req.method !== "POST") {
    return res.status(405).json({ error: "Method not allowed" });
  }

  try {

    const {
      currency,
      currentRevenue,
      targetRevenue,
      incomeGap,
      materialCost,
      overhead,
      netProfit,
      profitMargin,
      breakEvenRevenue,
      projectsNeeded
    } = req.body;

    const symbolMap = {
      USD: "$",
      EUR: "€",
      GBP: "£",
      AUD: "A$"
    };

    const symbol = symbolMap[currency] || "";

    const doc = new PDFDocument({
      size: "A4",
      margin: 50
    });

    res.setHeader("Content-Type", "application/pdf");
    res.setHeader(
      "Content-Disposition",
      "attachment; filename=Business-Profit-Analysis.pdf"
    );

    doc.pipe(res);

    // ===== TITLE =====
    doc
      .fontSize(22)
      .text("Business Profit Analysis", { align: "center" });

    doc.moveDown(2);

    // ===== REVENUE =====
    doc.fontSize(16).text("Revenue Overview", { underline: true });
    doc.moveDown();
    doc.fontSize(12);
    doc.text(`Current Revenue: ${symbol}${currentRevenue}`);
    doc.text(`Target Revenue: ${symbol}${targetRevenue}`);
    doc.text(`Income Gap: ${symbol}${incomeGap}`);

    doc.moveDown(2);

    // ===== COST STRUCTURE =====
    doc.fontSize(16).text("Cost Structure", { underline: true });
    doc.moveDown();
    doc.fontSize(12);
    doc.text(`Material Cost: ${symbol}${materialCost}`);
    doc.text(`Annual Overhead: ${symbol}${overhead}`);

    doc.moveDown(2);

    // ===== PROFITABILITY =====
    doc.fontSize(16).text("Profitability", { underline: true });
    doc.moveDown();
    doc.fontSize(12);
    doc.text(`Net Profit: ${symbol}${netProfit}`);
    doc.text(`Profit Margin: ${profitMargin}%`);
    doc.text(`Break-even Revenue: ${symbol}${breakEvenRevenue}`);

    doc.moveDown(2);

    // ===== OPERATIONS =====
    doc.fontSize(16).text("Operational Metrics", { underline: true });
    doc.moveDown();
    doc.fontSize(12);
    doc.text(`Projects Needed to Hit Target: ${projectsNeeded}`);

    doc.moveDown(3);

    doc
      .fontSize(10)
      .fillColor("gray")
      .text("Generated by Contractor Profit AI", { align: "center" });

    doc.end();

  } catch (error) {
    console.error(error);
    res.status(500).json({ error: "PDF generation failed" });
  }
}
